console.log(x===y);//Returns true if the operands are equal and have the same type
console.log(x!=y);//Returns true if the operands are not equal
console.log(x!==y);//Returns true if the operands are not equal, or have different types
console.log(x>y);//Returns true if the left operand is greater than the right operand
console.log(x>=y);//Returns true if the left operand is greater than or equal to the right operand
console.log(x<y);//Returns true if the left operand is less than the right operand
console.log(x<=y);//Returns true if the left operand is less than or equal to the right operand

